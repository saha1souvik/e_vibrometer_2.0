package com.example.Intelvib;

public class Point {

    private float xValue;
    private float yValue;

    public Point() {
    }

    public Point(float xValue, float yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public float getxValue() {
        return xValue;
    }

    public float getyValue() {
        return yValue;
    }
}
